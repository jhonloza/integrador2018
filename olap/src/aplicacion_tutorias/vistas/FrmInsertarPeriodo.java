    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
         Connection cn;
        CallableStatement cs;
        try {
            Class.forName("org.postgresql.Driver");
            cn = DriverManager.getConnection("jdbc:postgresql://localhost:5432/integrador", "postgres" ,"12345");
            cs=cn.prepareCall("SELECT actividades.fninsertar_periodo(?,?,?,?,?,?)");
          
            cs.setString(1 , txtnombre.getText());
            cs.setString(2 ,txtfecha_inicio.getText());
            //cs.setString(3 ,Date.parseDate(txtfecha_fin.getText()));
            cs.setString(4 ,txttipo.getText());
            cs.setString(5 ,txtobservaciones.getText());
            cs.setInt(6, Integer.parseInt(txtcodigo_sicoa.getText()));
           
            if (cs.execute()) {
                JOptionPane.showMessageDialog(null, "Datos ingresados correctamente");
                
            }
        } catch (ClassNotFoundException | SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

